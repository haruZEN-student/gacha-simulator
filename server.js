// ============================================================
// server.js - アプリのメインファイル（ここからすべてが始まる）
// ============================================================

// 必要なライブラリを読み込む
const express = require('express');      // Webサーバーを作るライブラリ
const path = require('path');            // ファイルパスを扱うNode.js標準ライブラリ

// Expressアプリを作成
const app = express();

// ポート番号の設定
// Render/Railwayは環境変数PORTを自動設定するので、それを優先する
const PORT = process.env.PORT || 3000;

// ============================================================
// ミドルウェアの設定（リクエストを処理する前段階の設定）
// ============================================================

// JSONデータを受け取れるようにする（POST送信されたデータの解析）
app.use(express.json());

// フォームデータを受け取れるようにする
app.use(express.urlencoded({ extended: true }));

// publicフォルダ内のファイルを静的ファイルとして公開する
// （CSS、JS、画像ファイルなどをブラウザからアクセスできるようにする）
app.use(express.static(path.join(__dirname, 'public')));

// ============================================================
// テンプレートエンジンの設定
// ============================================================

// EJSをテンプレートエンジンとして使用する
// EJSを使うとHTMLの中でJavaScriptが書けるようになる
app.set('view engine', 'ejs');

// テンプレートファイル（.ejsファイル）を入れるフォルダを指定
app.set('views', path.join(__dirname, 'views'));

// ============================================================
// ルーターの読み込み
// ============================================================

// ルーター（URLと処理の対応）を別ファイルから読み込む
const gachaRouter = require('./routes/gacha');

// '/api/gacha' で始まるURLはgachaRouterが処理する
app.use('/api/gacha', gachaRouter);

// ============================================================
// メインページのルート
// ============================================================

// トップページ（/）にアクセスされたらindex.ejsを表示する
app.get('/', (req, res) => {
  res.render('index', {
    title: 'STAR GACHA - 星霊召喚システム'
  });
});

// ============================================================
// サーバーの起動
// ============================================================

app.listen(PORT, () => {
  console.log('========================================');
  console.log(`🚀 サーバーが起動しました！`);
  console.log(`📡 URL: http://localhost:${PORT}`);
  console.log('========================================');
});

// Exportしてテストでも使えるようにする（オプション）
module.exports = app;
